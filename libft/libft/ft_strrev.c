#include "../include/libft.h"

void	ft_strrev(char **s, int size)
{
	int	aux;
	int	i;

	i = 0;
	while (i < (size / 2))
	{
		aux = *(*s + i);
		*(*s + i) = *(*s + (size - 1 - i));
		*(*s + (size - 1 - i)) = aux;
		i++;
	}
}
