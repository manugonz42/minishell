#include "../include/libft.h"

int	ft_isnumber(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (!((str[i] >= '0' && str[i] <= '9')
				|| str[i] == '-' /*|| str[i] == '.'*/)
			|| (str[i] == '-' && !(str[i + 1] >= '0' && str[i + 1] <= '9'))
			|| (str[i] == '.' && !(str[i + 1] >= '0' && str[i + 1] <= '9')))
			return (0);
		i++;
	}
	return (1);
}
